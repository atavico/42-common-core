#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		i;
	char		*dest;

	dest = (char *)malloc(sizeof(*s1) * (ft_strlen(s1) + 1));
	if (!dest)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}		
